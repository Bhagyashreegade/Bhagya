package com.company;

public class Assignment_4 {

    public static void main(String[] args) {
        // print no 1 to 100
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
        // print no 100 to 1
        for (int i = 100; i >= 1; i--) {
            System.out.println(i);
        }
        // skip one number
        for (int i = 1; i <= 100; i = i + 2) {
            System.out.println(i);
        }
            }
                }
